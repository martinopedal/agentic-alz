#!/usr/bin/env python3
"""Generate human-readable docs from the sources of truth.

The intent is to keep the prose, the schemas, the prompts, the policies, and
the model allowlist from drifting. Every generated file contains a "DO NOT
EDIT" banner and the SHA-256 of the inputs that produced it; CI re-runs this
script with ``--check`` and fails if the working tree is not byte-identical
to what would be regenerated.

Generated outputs (under ``docs/generated/``):

* ``schemas.md``    — every JSON Schema rendered as a property table.
* ``prompts.md``    — index of LLM prompts with stage/version metadata.
* ``policies.md``   — index of OPA policies with their package + summary.
* ``models.md``     — frontier model allowlist as a table.
* ``mcp.md``        — MCP server allowlist as a table.
* ``cli.md``        — ``agentic-alz --help`` output for every subcommand.

Usage:

    python scripts/gen_docs.py            # write
    python scripts/gen_docs.py --check    # exit non-zero if anything would change
"""

from __future__ import annotations

import argparse
import hashlib
import importlib.util
import io
import json
import re
import subprocess
import sys
from pathlib import Path
from typing import Any

import yaml

REPO_ROOT = Path(__file__).resolve().parent.parent
GENERATED = REPO_ROOT / "docs" / "generated"

BANNER = (
    "<!-- AUTOGENERATED by scripts/gen_docs.py from {sources}. "
    "DO NOT EDIT BY HAND. Regenerate with `python scripts/gen_docs.py`. "
    "Inputs SHA-256: {sha} -->\n\n"
)


def _sha(*paths: Path) -> str:
    h = hashlib.sha256()
    for p in sorted(paths):
        h.update(p.read_bytes())
    return h.hexdigest()


# ---------------------------------------------------------------------------
# schemas.md
# ---------------------------------------------------------------------------
def render_schemas() -> tuple[str, list[Path]]:
    schemas_dir = REPO_ROOT / "schemas"
    files = sorted(schemas_dir.glob("*.schema.json"))
    out = io.StringIO()
    out.write("# JSON Schemas\n\n")
    out.write(
        "Every typed contract crossing a stage boundary in Agentic ALZ is a "
        "JSON Schema in [`schemas/`](../../schemas/). This page is generated "
        "from those files.\n\n"
    )
    for f in files:
        schema = json.loads(f.read_text(encoding="utf-8"))
        out.write(f"## `{f.name}`\n\n")
        out.write(f"- **`$id`**: `{schema.get('$id', '—')}`\n")
        out.write(f"- **Title**: {schema.get('title', '—')}\n")
        if "description" in schema:
            out.write(f"\n{schema['description']}\n\n")
        else:
            out.write("\n")
        _render_properties(out, schema)
        out.write("\n")
    return out.getvalue(), files


def _render_properties(out: io.StringIO, schema: dict[str, Any]) -> None:
    props = schema.get("properties") or {}
    if not props:
        return
    required = set(schema.get("required") or [])
    out.write("| Property | Type | Required | Description |\n")
    out.write("| --- | --- | :---: | --- |\n")
    for name, sub in props.items():
        ty = _type_of(sub)
        req = "✓" if name in required else " "
        desc = (sub.get("description") or "").replace("\n", " ").strip()
        if "enum" in sub:
            desc = (desc + " " if desc else "") + f"Enum: `{sub['enum']}`."
        if "const" in sub:
            desc = (desc + " " if desc else "") + f"Constant: `{sub['const']}`."
        if "pattern" in sub:
            desc = (desc + " " if desc else "") + f"Pattern: `{sub['pattern']}`."
        out.write(f"| `{name}` | {ty} | {req} | {desc} |\n")
    out.write("\n")


def _type_of(node: dict[str, Any]) -> str:
    if "$ref" in node:
        return f"`{node['$ref'].rsplit('/', 1)[-1]}`"
    t = node.get("type")
    if isinstance(t, list):
        return " \\| ".join(f"`{x}`" for x in t)
    if t == "array":
        items = node.get("items") or {}
        return f"array of {_type_of(items)}"
    if t == "object":
        return "`object`"
    return f"`{t or 'any'}`"


