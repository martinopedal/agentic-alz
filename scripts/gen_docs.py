PROMPT_RX = re.compile(
    r"^# (?P<title>.+?)\n+> Stage: `(?P<stage>[^`]+)`\.?(?P<rest>.*?)$",
    re.DOTALL,
)


def render_prompts() -> tuple[str, list[Path]]:
    prompts_dir = REPO_ROOT / "prompts"
    files = sorted(prompts_dir.glob("*.md"))
    out = io.StringIO()
    out.write("# LLM prompts\n\n")
    out.write(
        "Versioned prompt files for the four LLM stages defined in the "
        "consensus plan. Each prompt declares its stage, its allowed tools, "
        "and its output contract. Source: [`prompts/`](../../prompts/).\n\n"
    )
    out.write("| File | Stage | First-line title |\n")
    out.write("| --- | --- | --- |\n")
    for f in files:
        text = f.read_text(encoding="utf-8")
        title = text.splitlines()[0].lstrip("# ").strip() if text else "—"
        m = re.search(r"Stage: `([^`]+)`", text)
        stage = m.group(1) if m else "—"
        out.write(f"| [`{f.name}`](../../prompts/{f.name}) | `{stage}` | {title} |\n")
    return out.getvalue(), files


# ---------------------------------------------------------------------------
# policies.md
# ---------------------------------------------------------------------------
def render_policies() -> tuple[str, list[Path]]:
    pol_dir = REPO_ROOT / "policies"
    files = sorted(pol_dir.glob("*.rego"))
    out = io.StringIO()
    out.write("# OPA / Conftest policies\n\n")
    out.write(
        "Deterministic guardrails enforced on every PR. Source: "
        "[`policies/`](../../policies/).\n\n"
    )
    out.write("| File | Package | One-line summary |\n")
    out.write("| --- | --- | --- |\n")
    for f in files:
        text = f.read_text(encoding="utf-8")
        pkg_m = re.search(r"^\s*package\s+([A-Za-z0-9_.]+)", text, re.MULTILINE)
        pkg = pkg_m.group(1) if pkg_m else "—"
        # First non-empty comment line.
        summary = "—"
        for line in text.splitlines():
            s = line.strip()
            if s.startswith("#") and len(s) > 2 and "policy" in s.lower():
                summary = s.lstrip("# ").rstrip(".")
                break
        out.write(f"| [`{f.name}`](../../policies/{f.name}) | `{pkg}` | {summary} |\n")
    return out.getvalue(), files


