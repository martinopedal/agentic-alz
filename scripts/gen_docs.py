# ---------------------------------------------------------------------------
# Driver
# ---------------------------------------------------------------------------
RENDERERS = {
    "schemas.md": render_schemas,
    "prompts.md": render_prompts,
    "policies.md": render_policies,
    "models.md": render_models,
    "cli.md": render_cli,
    "roadmap.md": render_roadmap,
    "README.md": render_index,
}


def build() -> dict[str, str]:
    """Return a {filename: content} map of everything that should be on disk."""
    out: dict[str, str] = {}
    for name, fn in RENDERERS.items():
        body, sources = fn()
        sha = _sha(*sources) if sources else "n/a"
        src_list = ", ".join(str(p.relative_to(REPO_ROOT)) for p in sources) or "(none)"
        out[name] = BANNER.format(sources=src_list, sha=sha) + body
    return out


def main(argv: list[str]) -> int:
    parser = argparse.ArgumentParser()
    parser.add_argument(
        "--check",
        action="store_true",
        help="Exit non-zero if any generated file is not byte-identical.",
    )
    args = parser.parse_args(argv[1:])

    GENERATED.mkdir(parents=True, exist_ok=True)
    desired = build()
    diffs: list[str] = []
    for name, content in desired.items():
        target = GENERATED / name
        existing = target.read_text(encoding="utf-8") if target.is_file() else ""
        if existing != content:
            diffs.append(name)
            if not args.check:
                target.write_text(content, encoding="utf-8")
    if args.check and diffs:
        print(
            "ERROR: generated docs are stale. Re-run: python scripts/gen_docs.py",
            file=sys.stderr,
        )
        for d in diffs:
            print(f"  - docs/generated/{d}", file=sys.stderr)
        return 1
    if diffs and not args.check:
        for d in diffs:
            print(f"wrote docs/generated/{d}")
    elif not diffs:
        print("docs/generated/ is up to date.")
    return 0


if __name__ == "__main__":
    sys.exit(main(sys.argv))
