def render_cli() -> tuple[str, list[Path]]:
    out = io.StringIO()
    out.write("# `agentic-alz` CLI reference\n\n")
    out.write(
        "Generated by invoking `agentic-alz <cmd> --help`. The CLI exposes "
        "the deterministic stages of the pipeline; LLM stages are exercised "
        "through the orchestrator runtime.\n\n"
    )
    sources = [REPO_ROOT / "orchestrator" / "agentic_alz" / "cli.py"]
    commands = [
        [],
        ["validate-inputs"],
        ["generate"],
        ["risk"],
        ["tf-policy"],
        ["judge"],
        ["models"],
    ]
    # Pin COLUMNS so click's help text wraps deterministically regardless of
    # the host terminal — required for `--check` to be reliable in CI.
    # PYTHONUTF8=1 forces the subprocess to use UTF-8 stdout/stderr on Windows
    # (where the default is the system ANSI codepage, e.g. cp1252 / cp437).
    env = {**__import__("os").environ, "COLUMNS": "100", "TERM": "dumb", "PYTHONUTF8": "1"}
    for cmd in commands:
        argv = ["agentic-alz", *cmd, "--help"]
        try:
            res = subprocess.run(
                argv, capture_output=True, text=True, encoding="utf-8", check=False, env=env
            )
            help_text = res.stdout or res.stderr
        except FileNotFoundError:
            help_text = "(agentic-alz CLI not installed)"
        label = "(root)" if not cmd else f"`agentic-alz {' '.join(cmd)}`"
        out.write(f"## {label}\n\n```\n{help_text.rstrip()}\n```\n\n")
    return out.getvalue(), sources


